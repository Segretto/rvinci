import json
import torch
from pathlib import Path
from typing import Dict, Any, Tuple

from rvinci.core.logging import get_logger

log = get_logger(__name__)


def parse_pose_dict(pose_dict: Dict[str, Any]) -> torch.Tensor:
    """
    Parses a Boston Dynamics style pose dictionary into a flat 7D torch Tensor (XYZ + Quat_WXYZ)
    Args:
        pose_dict: e.g. {"position": {"x": 0, "y": 0, "z": 0}, "rotation": {"w": 1, "x": 0, "y": 0, "z": 0}}
    Returns:
        torch.Tensor of shape (7,)
    """
    try:
        pos = pose_dict["position"]
        rot = pose_dict["rotation"]
        # Standardize the order to XYZ + WXYZ (or any consistent convention you prefer, Boston Dynamics uses w, x, y, z)
        return torch.tensor(
            [pos["x"], pos["y"], pos["z"], rot["w"], rot["x"], rot["y"], rot["z"]],
            dtype=torch.float32,
        )
    except KeyError as e:
        log.error(f"Missing key in pose dict: {e}")
        raise ValueError(f"Invalid pose dictionary format. Missing: {e}")


class ImitationDataset(torch.utils.data.Dataset):
    """
    A Dataset parser for the Spot bottleneck imitation logging format.
    """

    def __init__(self, data_dir: str | Path):
        self.data_dir = Path(data_dir)
        self.samples = []

        # Discover all .json files iteratively
        if not self.data_dir.exists():
            raise FileNotFoundError(f"Data directory {data_dir} does not exist.")

        # Glob for all JSON files inside the folder or subfolders
        self.json_paths = sorted(list(self.data_dir.rglob("*.json")))
        log.info(f"Discovered {len(self.json_paths)} JSON samples in {data_dir}")

    def __len__(self) -> int:
        return len(self.json_paths)

    def __getitem__(self, idx: int) -> Tuple[str, torch.Tensor, torch.Tensor]:
        """
        Returns:
            image_path (str): Relative path to the image
            wrist_pose (torch.Tensor): 7D proprioceptive input pose (XYZ + WXYZ)
            target_pose (torch.Tensor): 7D target bottleneck relative pose (XYZ + WXYZ)
        """
        json_path = self.json_paths[idx]
        with open(json_path, "r") as f:
            data = json.load(f)

        img_path = str(json_path.parent / Path(data["image_filename"]).name)

        wrist_pose = parse_pose_dict(data["wrist_frame"])
        target_pose = parse_pose_dict(data["hand_tform_bottleneck"])

        return img_path, wrist_pose, target_pose


class OfflineFeatureDataset(torch.utils.data.Dataset):
    """
    A Dataset parser for the pre-extracted features generated by pipeline/generate_dataset.py.
    """

    def __init__(self, data_path: str | Path):
        self.data_path = Path(data_path)

        if not self.data_path.exists():
            raise FileNotFoundError(f"Feature dataset {data_path} does not exist.")

        log.info(f"Loading offline feature dataset from {data_path}")

        if self.data_path.suffix == ".pt":
            self.data = torch.load(self.data_path)
        elif self.data_path.suffix == ".pkl":
            import pickle

            with open(self.data_path, "rb") as f:
                self.data = pickle.load(f)
        else:
            raise ValueError(
                f"Unsupported dataset format: {self.data_path.suffix}. Expected .pt or .pkl."
            )

        self.length = len(self.data)
        log.info(f"Loaded {self.length} samples.")

    def __len__(self) -> int:
        return self.length

    def __getitem__(self, idx: int) -> Tuple[torch.Tensor, torch.Tensor, torch.Tensor]:
        """
        Returns:
            visual_feature (torch.Tensor): The pre-extracted visual latent (e.g., query vector)
            wrist_pose (torch.Tensor): 7D proprioceptive input pose
            target_pose (torch.Tensor): 7D target pose (relative offset)
        """
        sample = self.data[idx]

        # Depending on how it was saved, it might be a numpy array or torch tensor
        vf = sample["visual_feature"]
        wp = sample["wrist_pose"]
        tp = sample["target_pose"]

        visual_feature = (
            torch.tensor(vf, dtype=torch.float32)
            if not isinstance(vf, torch.Tensor)
            else vf
        )
        wrist_pose = (
            torch.tensor(wp, dtype=torch.float32)
            if not isinstance(wp, torch.Tensor)
            else wp
        )
        target_pose = (
            torch.tensor(tp, dtype=torch.float32)
            if not isinstance(tp, torch.Tensor)
            else tp
        )

        return visual_feature, wrist_pose, target_pose
